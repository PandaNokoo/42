#include "skyscraper.h"

void	affichage(char tab[6][6])
{
	int	x;
	int	y;

	x = 1;
	while (x < 5)
	{
		y = 1;
		while (x != 5 && y < 5)
		{
			write(1, &tab[x][y], 1);
			y++;
		}
		x++;
	}
}
