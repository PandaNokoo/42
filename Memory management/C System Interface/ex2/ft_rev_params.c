#include <unistd.h>

void	ft_rev_params(int argc, char **argv)
{
	char	*temp;
	int		debut;
	int		fin;

	debut = 1;
	fin = argc - 1;
	while (debut < fin)
	{
		temp = argv[debut];
		argv[debut] = argv[fin];
		argv[fin] = temp;
		debut++;
		fin--;
	}
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	ft_rev_params(argc, argv);
	while (i < argc)
	{
		ft_putstr(argv[i]);
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
